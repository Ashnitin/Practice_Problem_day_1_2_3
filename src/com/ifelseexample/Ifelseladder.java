package com.ifelseexample;

public class Ifelseladder {
    public static void main(String[] args) {
        String s="Mumbai";
        if(s=="pune")
        {
            System.out.println("Welcome to pune");
        } else if (s=="banglore") {
            System.out.println("Welcome to banglore");
        }
        else {
            System.out.println("Welcome to mumbai");
        }
    }
}
