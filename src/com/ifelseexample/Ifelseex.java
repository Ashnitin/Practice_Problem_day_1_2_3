package com.ifelseexample;

public class Ifelseex {
    public static void main(String[] args) {


        int x = 10;
        int y = 20;
        if (x + y > 40) {
            System.out.println("x+y greater than 40");
        } else {
            System.out.println("x+y less than 40");
        }
    }

}
